// Helper function to get CPI value for a specific date
export const getCPIValue = (dateString) => {
  const date = new Date(dateString);
  // Format to match the data format (M/YYYY) - no padding for month
  const monthYear = `${date.getMonth() + 1}/${date.getFullYear()}`;
  console.log(`Looking for CPI entry with month/year: ${monthYear}`);
  
  const cpiEntry = cpiData.find(entry => entry.month === monthYear);
  if (!cpiEntry) {
    console.log(`No CPI entry found for ${monthYear}`);
  } else {
    console.log(`Found CPI entry: `, cpiEntry);
  }
  
  return cpiEntry ? cpiEntry.value : null;
}; 